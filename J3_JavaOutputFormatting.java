/*
สามารถใช้ฟังก์ชัน System.out.printf ของ Java เพื่อพิมพ์เอาต์พุตที่จัดรูปแบบได้ จุดประสงค์ของแบบฝึกหัดนี้คือเพื่อทดสอบความเข้าใจของคุณเกี่ยวกับการจัดรูปแบบเอาต์พุตโดยใช้ printf
เพื่อให้คุณเริ่มต้นได้ ส่วนหนึ่งของโซลูชันมีให้ในตัวแก้ไข คุณต้องจัดรูปแบบและพิมพ์อินพุตเพื่อทำโซลูชันให้เสร็จสมบูรณ์
รูปแบบการป้อนข้อมูล
ทุกบรรทัดที่ป้อนจะมีสตริงตามด้วยจำนวนเต็ม
แต่ละสตริงจะมีอักขระที่เป็นตัวอักษรได้สูงสุด 10 และแต่ละจำนวนเต็มจะอยู่ในช่วงรวมตั้งแต่่ 0 ถึง 999
รูปแบบเอาต์พุต
ในแต่ละบรรทัดของเอาต์พุตควรมีสองคอลัมน์:
คอลัมน์แรกมีสตริงและถูกเว้นไว้โดยใช้อักขระทั้งหมด
คอลัมน์ที่สองมีจำนวนเต็มซึ่งแสดงเป็นตัวเลขทั้งหมด หากอินพุตดั้งเดิมมีตัวเลขน้อยกว่าสามหลัก คุณต้องรองเลขศูนย์นำหน้าเอาต์พุตด้วยศูนย์

อินพุตตัวอย่าง
java 100
cpp 65
python 50

เอาต์พุตตัวอย่าง
================================
java           100
cpp            065
python         050
================================
 */

import java.util.Scanner;
public class J3_JavaOutputFormatting {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        System.out.println("================================");
        for(int i=0;i<3;i++)
        {
            String s1=sc.next();
            int x=sc.nextInt();
            //Complete this line
            System.out.printf("%-15s%03d%n", s1, x);
        }
        System.out.println("================================");

    }
}

/*
โค้ดด้านบนเป็นโปรแกรมที่ใช้ในการรับข้อมูลจากผู้ใช้และพิมพ์ผลลัพธ์ออกทางหน้าจอ โดยมีการใช้ลูป for เพื่อรับค่าสตริงและจำนวนเต็มจากผู้ใช้
ซึ่งจำนวนรอบในการรับข้อมูลจะเป็น 3 รอบตามที่กำหนดในลูป for
ในแต่ละรอบของลูป for เราใช้ Scanner เพื่อรับค่าสตริงจากผู้ใช้ผ่าน sc.next() และรับค่าจำนวนเต็มผ่าน sc.nextInt() จากนั้นเราใช้ System.out.printf()
เพื่อพิมพ์ผลลัพธ์ออกทางหน้าจอ โดยใช้รูปแบบการจัดรูปแบบ (%) เพื่อกำหนดความกว้างและรูปแบบของข้อมูลที่จะแสดงผล โดยในที่นี้จะใช้ %-15s
เพื่อกำหนดให้สตริงมีความกว้าง 15 ตัวอักษรและจัดชิดซ้าย และ %03d เพื่อกำหนดให้จำนวนเต็มมีความกว้าง 3 หลักและเติมเลขศูนย์ข้างหน้ากรณีที่มีน้อยกว่า 3 หลัก
โดยสรุปแล้ว โปรแกรมนี้จะรับข้อมูลสตริงและจำนวนเต็มจากผู้ใช้ และพิมพ์ผลลัพธ์ออกทางหน้าจอในรูปแบบตามที่โจทย์กำหนด
 */

/*
System.out.printf() เป็นเมธอดในคลาส PrintStream ที่ใช้ในการพิมพ์ผลลัพธ์ออกทางหน้าจอโดยกำหนดรูปแบบการจัดรูปแบบ (format)
ของข้อมูลที่จะแสดงผล รูปแบบการจัดรูปแบบถูกกำหนดด้วยพารามิเตอร์ที่ตามหลังสตริงข้อความที่ต้องการพิมพ์ โดยใช้ตัวอักษรพิเศษ (%) เพื่อระบุประเภทข้อมูลและรูปแบบที่ต้องการ
ในกรณีนี้ "%-15s%03d%n" คือรูปแบบการจัดรูปแบบที่กำหนดให้พิมพ์สตริงและจำนวนเต็มออกทางหน้าจอ โดยแต่ละส่วนของรูปแบบจะมีความหมายดังนี้:
%-15s: จัดชิดซ้าย (-) และกำหนดความกว้างของสตริงที่จะแสดงผลให้มีความกว้าง 15 ตัวอักษร
%03d: กำหนดความกว้างของจำนวนเต็มที่จะแสดงผลให้มีความกว้าง 3 หลักและเติมเลขศูนย์ข้างหน้ากรณีที่มีน้อยกว่า 3 หลัก
%n: ใช้ในการขึ้นบรรทัดใหม่
ดังนั้น คำสั่ง System.out.printf("%-15s%03d%n", s1, x); จะทำการพิมพ์สตริง s1 โดยจัดชิดซ้ายและกำหนดความกว้าง 15 ตัวอักษร ตามด้วยจำนวนเต็ม x
ที่เติมเลขศูนย์ข้างหน้าและกำหนดความกว้าง 3 หลัก และสุดท้ายพิมพ์เครื่องหมายขึ้นบรรทัดใหม่ (%n)
 */